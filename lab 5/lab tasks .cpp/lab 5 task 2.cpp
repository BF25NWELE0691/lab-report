#include <iostream> // include input-output library
using namespace std;

int main() {
    int i = 1; // initialize counter variable to 1

    // do-while loop to display numbers from 1 to 10
    do {
        cout << i << endl; // print current value of i on a new line
        i++; // increment i by 1
    } while (i <= 10); // check condition after executing the loop

    return 0; // end of program
}
